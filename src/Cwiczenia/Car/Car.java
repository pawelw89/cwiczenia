package Cwiczenia.Car;

public class Car {

    public String marka;
    public String model;
    public int rok;
    public int przebieg;

    public Car(String marka, String model, int rok, int przebieg) {
        this.marka = marka;
        this.model = model;
        this.rok = rok;
        this.przebieg = przebieg;
    }
}
